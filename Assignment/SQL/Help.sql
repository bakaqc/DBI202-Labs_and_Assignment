CREATE PROCEDURE Pay_Help(@Bill_ID VARCHAR(10))
AS
BEGIN

	DECLARE @Create_Date DATE = NULL
	DECLARE @Create_Time TIME = NULL

	SELECT @Create_Date = Create_Date, @Create_Time = Create_Time
	FROM Bill
	WHERE Bill_ID = @Bill_ID

	-- Update Bill with @Bill_ID
	UPDATE	Bill
	SET		Payment_Date	= @Create_Date,
			Payment_Time	= @Create_Time
	
	WHERE	Bill_ID			= @Bill_ID
END
GO

